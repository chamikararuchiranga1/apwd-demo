function clickTownEvent () {
    document.getElementById('clickTown').innerHTML = "Panadura";
}

function onChange (value) {
    document.getElementById('onChangeLabel').innerHTML = value;
}

function onMouseOver () {
    document.getElementById('mouseOver').style.backgroundColor = '#e85555';
}

function onMouseOut () {
    document.getElementById('mouseOver').style.backgroundColor = '#b9b7b7';
}

function onKeyUp (value) {
    document.getElementById('onKeyPress').innerHTML = value;
}